
public class Number {

	public static void main(String[] args) {
		//Operatior
		System.out.println(6+2);
		System.out.println(6-2);
		System.out.println(6*2);
		System.out.println(6/2);
		
		System.out.println(Math.PI);
		System.out.println(Math.floor(Math.PI)); //소수점 자르기
		System.out.println(Math.ceil(Math.PI)); //수 올리기

	}

}
